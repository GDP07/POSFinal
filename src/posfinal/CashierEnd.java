    private void DisInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_DisInputKeyReleased
        int searchkeycode =evt.getKeyCode();
        switch (searchkeycode){
            case 27:
                F.setEnableRec(jPanel2, true);
                DiscountPanel.setVisible(false);
                F.IsCartEmpty();
                break;
            default:
                if (!"".equals(DisInput.getText())) {
                    if (Pattern.matches(F.ValidateDouble, DisInput.getText())) {
                        if (F.SelectedDisType == 2 && Double.valueOf(DisInput.getText()) > 100) {
                            DisInput.setForeground(Color.red);
                            DiscountOK.setEnabled(false);
                        } else {
                            DisInput.setForeground(Color.white);
                            DiscountOK.setEnabled(true);
                        }

                    } else {
                        DisInput.setForeground(Color.red);
                        DiscountOK.setEnabled(false);
                    }
                } else {
                    DisInput.setForeground(Color.white);
                    DiscountOK.setEnabled(true);
                }
                break;
        }
        
    }//GEN-LAST:event_DisInputKeyReleased

    private void QTYEnterKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_QTYEnterKeyReleased
        int searchkeycode =evt.getKeyCode();
        switch (searchkeycode){
            case 27:
                F.setEnableRec(jPanel2, true);
                QTYWindow.setVisible(false);
                F.IsCartEmpty();
                break;
            default :
                if (!"".equals(QTYEnter.getText())) {
                    if (Pattern.matches(F.ValidateDouble, QTYEnter.getText())) {
                        QTYEnter.setForeground(Color.black);
                        QTYOK.setEnabled(true);
                    } else {
                        QTYEnter.setForeground(Color.red);
                        QTYOK.setEnabled(false);
                    }
                }
                break;
        }
        
    }//GEN-LAST:event_QTYEnterKeyReleased

