    private void CashInKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CashInKeyReleased
        if(CashIn.isEnabled()){
            int searchkeycode =evt.getKeyCode();
            switch (searchkeycode){
                case 116:
                    if (Payment.isEnabled()) {
                        if ("0.0".equals(CashIn.getText())) {
                            F.ST(CashIn);
                        } else {
                            int response = JOptionPane.showConfirmDialog(this, "End Sale ?", "Select option", JOptionPane.YES_OPTION);
                            if (response == JOptionPane.YES_OPTION) {
                                F.ProceedPayment();
                            }
                        }
                    }
                    break;
                default:
                    if (!"".equals(CashIn.getText())) {
                        if (Pattern.matches(F.ValidateDouble, CashIn.getText())) {
                            CashIn.setForeground(Color.white);
                            Bal.setText(String.valueOf(F.RoundDouble(Double.valueOf(CashIn.getText())) - Double.valueOf(NetTot.getText())));
                            F.IsCartEmpty();
                        } else {
                            CashIn.setForeground(Color.red);
                            Payment.setEnabled(false);
                        }
                    } else {
                        Bal.setText("0.0");
                        Payment.setEnabled(false);
                    }
            }
        }
    }//GEN-LAST:event_CashInKeyReleased

