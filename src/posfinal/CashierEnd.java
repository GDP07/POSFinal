    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        if ("".equals(Search.getText())){
            Search.setText("");
            placeholder1.setText(F.SearchTempLable);
        }else if((F.ChangeSearchType==1||F.ChangeSearchType==2)&&!"".equals(Search.getText())){
            if (Pattern.matches(F.ValidateInt, Search.getText())){
                Search.setBorder(new LineBorder(Color.white, 1));
                F.EnteredQR=true;
                F.Search(Search.getText(),SearchTable,placeholder1);
                Search.setText("");
                placeholder1.setText(F.SearchTempLable);
            }else{
                Search.setBorder(new LineBorder(Color.red, 1));
            }
        } 
    }//GEN-LAST:event_SearchActionPerformed

